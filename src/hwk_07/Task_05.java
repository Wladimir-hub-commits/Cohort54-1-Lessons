/*
Task 5 * (Опционально)
Используйте оператор switch для решения задачи:

Ваш ребенок принес оценку за контрольную работу по математике. В школе 12-балльная система. Запишите в переменную случайное число от 0 до 12.

Вы разрешаете ребенку смотреть телевизор не более 60 минут в день. На сегодня у него осталось 45 минут.

Если оценка от 10 до 12 баллов: скажите ребенку, как вы рады, и добавьте к оставшемуся времени 1 час.
Оценка от 7 до 9 баллов: похвалите ребенка и добавьте 45 минут.
Оценка от 4 до 6 баллов: нейтрально отнеситесь и добавьте 15 минут.
Оценка 3 балла: огорчитесь и вычтите 30 минут.
Оценка меньше 3 баллов: запретите телевизор на сегодня.
Выведите на экран оставшееся время для просмотра ТВ.

P.S. Проконтролируйте, чтобы ребенок не смотрел ТВ больше 60 минут в день.
 */
public class Task_05 {
    public static void main(String[] args) {


        // Генерируем случайное число от 0 до 12
        Random random = new Random();
        int score = random.nextInt(13); // 0-12

        // Начальное время просмотра ТВ
        int remainingTime = 45; // минуты

        // Вывод оценки
        System.out.println("Оценка: " + score);

        // Используем switch для обработки оценки
        switch (score) {
            case 10:
            case 11:
            case 12:
                System.out.println("Отличная работа! Я так горжусь тобой!");
                remainingTime += 60; // добавляем 1 час
                break;
            case 7:
            case 8:
            case 9:
                System.out.println("Хорошо сделано! Ты молодец!");
                remainingTime += 45; // добавляем 45 минут
                break;
            case 4:
            case 5:
            case 6:
                System.out.println("Неплохо, но есть над чем работать.");
                remainingTime += 15; // добавляем 15 минут
                break;
            case 3:
                System.out.println("Мне грустно, что ты не старался.");
                remainingTime -= 30; // вычитаем 30 минут
                break;
            default: // score < 3
                System.out.println("Сегодня телевизор под запретом.");
                remainingTime = 0; // запретить телевизор
                break;
        }

        // Контроль, чтобы не было более 60 минут
        if (remainingTime > 60) {
            remainingTime = 60; // максимальное время - 60 минут
        }

        // Вывод оставшегося времени для просмотра ТВ
        System.out.println("Оставшееся время для просмотра телевизора: " + remainingTime + " минут.");
    }
}
